def create_and_save_flow_matrix(flows, num_departments, filename):
    # 初始化 N x N 的零矩阵
    matrix = np.zeros((num_departments, num_departments))

    # 填充物流量
    for i, j, flow in flows:
        # 注意：原文献索引从1开始，Python数组索引从0开始，因此需要减1
        if i <= num_departments and j <= num_departments:
            matrix[i - 1][j - 1] = flow

            # 在某些算法（如基于遗传算法的 UA-FLP）中，通常会将有向流量转为对称的距离惩罚权重。
            # 若你需要一个对称矩阵（即 f_ij 和 f_ji 相等），请取消注释下一行：
            # matrix[j-1][i-1] = flow

    # 将矩阵转换为 DataFrame，方便保存为 CSV 并加上行列的部门编号（D1 到 Dn）
    columns = [f"D{i}" for i in range(1, num_departments + 1)]
    df = pd.DataFrame(matrix, index=columns, columns=columns)

    # 保存到 CSV
    df.to_csv(filename)
    print(f"成功生成并保存 {filename} ！矩阵尺寸: {num_departments} x {num_departments}")


# 1. 生成 SC30 的 CSV 文件
